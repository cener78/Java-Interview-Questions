package JavaInterviewQuestions.src;

public class Java_05_QuotientAndRemainder {

    public static void main(String[] args) {

        int dividend = 30;
        int division = 4;

        int quotient = dividend / division; //quotient bolum
        int remainder = dividend % division; // remainder kalan

        System.out.println(quotient);

        System.out.println(remainder);

    }

}
